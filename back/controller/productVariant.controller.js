export const updateProductVariant = async (req, res) => {
  try {
    const { id } = req.params;
    const { color, colorCode, isDefault, options, price, stock, discount, weight, dimensions, isActive } = req.body;

    if (req.user.role !== "admin") {
      return sendForbiddenResponse(res, "Access denied. Only admin can update variants!");
    }

    if (!mongoose.Types.ObjectId.isValid(id)) {
      return sendBadRequestResponse(res, "Invalid ProductVariant ID.");
    }

    const existingVariant = await ProductVariant.findById(id);
    if (!existingVariant) {
      return sendNotFoundResponse(res, "ProductVariant not found.");
    }

    const updateData = {};
    if (color) updateData.color = color;
    if (colorCode !== undefined) updateData.colorCode = colorCode;
    if (discount !== undefined) updateData.discount = discount;
    if (weight !== undefined) updateData.weight = weight;
    if (isActive !== undefined) updateData.isActive = isActive;

    if (dimensions !== undefined) {
      try {
        updateData.dimensions = typeof dimensions === "string" ? JSON.parse(dimensions) : dimensions;
      } catch (err) {
        updateData.dimensions = null;
      }
    }

    if (isDefault !== undefined) {
      const defaultValue = isDefault === true || isDefault === "true";
      if (defaultValue) {
        await ProductVariant.updateMany({ productId: existingVariant.productId }, { isDefault: false });
      }
      updateData.isDefault = defaultValue;
    }

    let willBeSizeless = existingVariant.options.length === 0;

    if (options !== undefined) {
      let parsedOptions = [];
      if (options) {
        try {
          parsedOptions = typeof options === "string" ? JSON.parse(options) : options;
        } catch (err) {
          parsedOptions = [];
        }
      }
      willBeSizeless = !Array.isArray(parsedOptions) || parsedOptions.length === 0;

      if (!willBeSizeless) {
        updateData.options = parsedOptions;
        updateData.price = null;
        updateData.stock = null;
      } else {
        updateData.options = [];
      }
    } else {
      if (price !== undefined || stock !== undefined) {
        willBeSizeless = true;
        updateData.options = [];
      }
    }

    if (willBeSizeless) {
      if (price !== undefined && price !== "") updateData.price = Number(price);
      if (stock !== undefined && stock !== "") updateData.stock = Number(stock);

      const finalPrice = updateData.price !== undefined ? updateData.price : existingVariant.price;
      const finalStock = updateData.stock !== undefined ? updateData.stock : existingVariant.stock;

      if (finalPrice === null || finalPrice === undefined || finalStock === null || finalStock === undefined) {
        return sendBadRequestResponse(res, "If no sizes are provided, price and stock are mandatory.");
      }
    }

    // Handle image updates — merge existing + new uploads preserving order
    if (req.body.existingImages !== undefined || (req.files && req.files.length > 0)) {
      // Parse existing images the admin wants to keep (in their chosen order)
      let keptImages = [];
      if (req.body.existingImages) {
        try {
          keptImages = typeof req.body.existingImages === "string"
            ? JSON.parse(req.body.existingImages)
            : req.body.existingImages;
        } catch {
          keptImages = [];
        }
      }

      // Delete S3 images that were removed by admin
      if (existingVariant.images?.length > 0) {
        const removedImages = existingVariant.images.filter(url => !keptImages.includes(url));
        if (removedImages.length > 0) {
          const removedKeys = removedImages.map(url => {
            const split = url.split(".amazonaws.com/");
            return split.length > 1 ? split[1] : null;
          }).filter(Boolean);
          if (removedKeys.length > 0) await deleteManyFromS3(removedKeys);
        }
      }

      // Upload new images
      const newlyUploaded = [];
      if (req.files && Array.isArray(req.files) && req.files.length > 0) {
        for (const file of req.files) {
          const uploaded = await uploadFile(file);
          newlyUploaded.push(uploaded.url);
        }
      }

      // Parse position map sent from frontend: array of {type:'existing'|'new', index:number}
      // If no positionMap, append new images after kept ones
      let finalImages = [];
      if (req.body.imagePositions) {
        try {
          const positions = typeof req.body.imagePositions === "string"
            ? JSON.parse(req.body.imagePositions)
            : req.body.imagePositions;

          let newIdx = 0;
          for (const pos of positions) {
            if (pos.type === "existing") {
              finalImages.push(keptImages[pos.index]);
            } else if (pos.type === "new") {
              finalImages.push(newlyUploaded[newIdx++]);
            }
          }
        } catch {
          finalImages = [...keptImages, ...newlyUploaded];
        }
      } else {
        finalImages = [...keptImages, ...newlyUploaded];
      }

      updateData.images = finalImages.filter(Boolean);
    }

    const updated = await ProductVariant.findByIdAndUpdate(id, updateData, { new: true, runValidators: true });

    return sendSuccessResponse(res, "Variant updated successfully.", updated);
  } catch (error) {
    console.error("Update Variant Error:", error);
    return sendErrorResponse(res, 500, error.message);
  }
};

export const deleteProductVariant = async (req, res) => {
  try {
    const { id } = req.params;

    if (req.user.role !== "admin") {
      return sendForbiddenResponse(res, "Access denied. Only admin can delete variants!");
    }

    if (!mongoose.Types.ObjectId.isValid(id)) {
      return sendBadRequestResponse(res, "Invalid ProductVariant ID!");
    }

    const variant = await ProductVariant.findById(id);
    if (!variant) return sendNotFoundResponse(res, "ProductVariant not found!");

    await Product.findByIdAndUpdate(variant.productId, {
      $pull: { variants: variant._id },
    });

    // Delete associated images from S3
    if (variant.images && variant.images.length > 0) {
      const imageKeys = variant.images.map(url => {
        const split = url.split(".amazonaws.com/");
        return split.length > 1 ? split[1] : null;
      }).filter(Boolean);

      if (imageKeys.length > 0) {
        await deleteManyFromS3(imageKeys);
      }
    }

    await ProductVariant.findByIdAndDelete(id);

    return sendSuccessResponse(res, "ProductVariant deleted successfully!");
  } catch (error) {
    return sendErrorResponse(res, 500, error.message);
  }
};

